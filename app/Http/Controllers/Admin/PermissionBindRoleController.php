     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $permission = Permission::find($id);
        $roles = Role::with(['permissions'=>function($q) use ($id) {
            $q->where('id', $id);
        } ])
            ->get();

        return view('admin.permissions.roles.index', compact('roles','permission'));
    }

